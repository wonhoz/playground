    private static Level Make(int n, string title, params (int x, int y, int z)[] cells)
    {
        var g = new bool[N, N, N];
        foreach (var (x, y, z) in cells)
            g[x, y, z] = true;

        return new Level
        {
            Number      = n,
            Title       = title,
            FrontTarget = Project(g, axis: 2), // XY
            TopTarget   = Project(g, axis: 1), // XZ
            SideTarget  = Project(g, axis: 0), // YZ
        };
    }

    // axis=2: XY 투영 (z-OR) → [x, y]
    // axis=1: XZ 투영 (y-OR) → [x, z]
    // axis=0: YZ 투영 (x-OR) → [y, z]
    private static bool[,] Project(bool[,,] g, int axis)
    {
        var s = new bool[N, N];
        for (int i = 0; i < N; i++)
        for (int j = 0; j < N; j++)
        for (int k = 0; k < N; k++)
        {
            bool v = axis switch
            {
                0 => g[k, i, j], // YZ: i=y, j=z, k=x
                1 => g[i, k, j], // XZ: i=x, j=z, k=y
                _ => g[i, j, k], // XY: i=x, j=y, k=z
            };
            if (v) { s[i, j] = true; break; }
        }
        return s;
    }
